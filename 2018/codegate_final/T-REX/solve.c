unsigned long long FieldAdd(unsigned long long addend_a, unsigned long long addend_b)
{
	return addend_a ^ addend_b;
}

unsigned long long FieldMul(unsigned long long multiplicand, unsigned long long multiplier, unsigned long long modulus)
{
	unsigned long long d_mask;
	unsigned long long result;

	d_mask = (unsigned long long)1 << 63;

	while (!(d_mask & modulus))
		d_mask >>= 1;

	result = 0;

	while (multiplier) {
		if (multiplier & 1)
			result ^= multiplicand;

		multiplier >>= 1;

		multiplicand <<= 1;

		if (multiplicand & d_mask)
			multiplicand ^= modulus;
	}

	return result;
}

unsigned long long FieldDivide(unsigned long long dividend, unsigned long long divisor, unsigned long long *remainder)
{
	unsigned long long quotient = 0;

	int degree = 63;

	while (!(divisor & 0x8000000000000000)) {
		divisor <<= 1;
		degree--;
	}

	quotient = 0;

	for (int bit = 63; bit >= degree; --bit) {
		if (dividend & ((unsigned long long)1 << bit)) {
			quotient |= ((unsigned long long)1 << (bit - degree));

			dividend ^= divisor;
		}

		divisor >>= 1;
	}

	*remainder = dividend;

	return quotient;
}

unsigned long long FieldEGCD(unsigned long long a, unsigned long long b, unsigned long long primitive, unsigned long long *r, unsigned long long *s)
{
	unsigned long long sum;
	unsigned long long product;
	unsigned long long quotient;
	unsigned long long remainder;
	unsigned long long gcd = 0;

	unsigned long long r_a = 1, s_a = 0;
	unsigned long long r_b = 0, s_b = 1;

	while (a && b) {
		if (a > b) {
			quotient = FieldDivide(a, b, &remainder);
			a = remainder;


			product = FieldMul(quotient, r_b, primitive);
			r_a = FieldAdd(r_a, product);

			product = FieldMul(quotient, s_b, primitive);
			s_a = FieldAdd(s_a, product);
		}
		else {
			quotient = FieldDivide(b, a, &remainder);
			b = remainder;

			product = FieldMul(quotient, r_a, primitive);
			r_b = FieldAdd(r_b, product);

			product = FieldMul(quotient, s_a, primitive);
			s_b = FieldAdd(s_b, product);
		}
	}

	if (a) {
		*s = s_a;
		*r = r_a;
		return a;
	}
	else {
		*s = s_b;
		*r = r_b;
		return b;
	}
}

unsigned long long FieldInvert(unsigned long long a, unsigned long long primitive)
{
	unsigned long long r, s;

	FieldEGCD(a, primitive, primitive, &r, &s);

	return r;
}


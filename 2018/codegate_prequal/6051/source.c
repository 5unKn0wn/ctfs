void strtobin(char* str, char* dst, int len)
{
	int bin_int[8];
	char chr;

	memset(dst, 0, len * BINSIZE + 1);
	for (int i = 0; i < len * BINSIZE; i += BINSIZE) {
		chr = str[i / BINSIZE];
		for (int j = 0; j < BINSIZE; j++) {
			bin_int[j] = chr % 2;
			chr /= 2;
		}
		for (int j = 0; j < BINSIZE; j++) {
			dst[i + j] = bin_int[BINSIZE - 1 - j] + 0x30;
		}
	}

	return;
}

